void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int i;
	int j;

	argc = 1;
	i = 1;
	while (argv[i])
	{
		j = 0;
		while (argv[i][j])
		{
			ft_putchar(argv[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}
